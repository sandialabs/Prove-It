def varRange(var, start, end):
    from proveit import safeDummyVar, IndexedVar
    param = safeDummyVar(var)
    return ExprRange(param, IndexedVar(var, param), start, end)

class RangeInstanceError(Exception):
    def __init__(self, msg):
        self.msg = msg
    def __str__(self):
        return self.msg
